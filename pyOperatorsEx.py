x = 5

# ex 1 - (same as)	x = x + 3

x += 3
print(x)

# ex 2 - (same as) x = x - 3
x -= 3
print(x)

# ex 3 - (same as) 	x = x * 3
x *= 3
print(x)

# ex 4 - (same as) 	x = x / 3
x /= 3
print(x)

# ex 5 - (same as) x = x % 3
x %= 3
print(x)

# ex 6 - (same as) x = x // 3
x //= 3
print(x)

# ex 7 - (same as) x = x ** 3
x **= 3
print(x)

# ex 8 - (same as) x = x & 3 // (AND	Sets each bit to 1 if both bits are 1)
x &= 3
print(x)

# ex 9 - (same as) x = x | 3 // (OR	Sets each bit to 1 if one of two bits is 1)
x |= 3
print(x)

# ex 10 - (same as)  x = x ^ 3 // (XOR	Sets each bit to 1 if only one of two bits is 1)
x ^= 3
print (x)

# ex 11 - (same as) x = x >> 3 // (Zero fill left shift->	Shift left by pushing zeros in from the right and let the leftmost bits fall off)
x >>= 3
print(x)

# ex 12 - (same as) x = x << 3 // (Signed right shift -> Shift right by pushing copies of the leftmost bit in from the left, and let the rightmost bits fall off)
x <<= 3
print(x)

